package org.f1x.log.file;

import java.io.*;

public class BufferedOutputStreamFactory implements OutputStreamFactory {
    private final int bufferSize;
    private final boolean append;

    public BufferedOutputStreamFactory(int bufferSize, boolean append) {
        this.bufferSize = bufferSize;
        this.append = append;
    }

    @Override
    public OutputStream create(File file) {
        try {
            OutputStream os = new FileOutputStream(file, append);
            if (bufferSize > 0)
                os = new BufferedOutputStream(os, bufferSize);
            return os;
        } catch (FileNotFoundException e) {
            throw new RuntimeException("Can't create a log file", e);
        }
    }
}
